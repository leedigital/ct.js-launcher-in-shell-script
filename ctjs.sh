download() {
   local fileName urlForDownload \
      localVersion dt
   ctjsVersion=$(echo "${jsonArray[tag_name]}" | sed -uE 's/(\s|\,)+//g')
   urlForDownload=$(echo "${jsonArray[browser_download_url]}" | tr -d '"')
   fileName=$(echo "${jsonArray[name]}" | tr -d '"'',')
   dt="$downloadTool $(echo "$urlForDownload" | tr -d '"')"
   if grep "/curl" <(echo "$dt") &>/dev/null; then
      dt+=" -O"
   fi
   # if versionFile exists and is not empty
   if [[ -s "$versionFile" ]]; then
      localVersion=$(cat <"$versionFile" |
         sed -uE 's/(\s|\,)+//g' |
         grep ^'"tag_name"' | cut -d: -f2)
      if [[ $localVersion != "$ctjsVersion" ]]; then
         # download file
         downloadAndExtractFile "$fileName" "$urlForDownload" "$dt"
      fi
   else
      downloadAndExtractFile "$fileName" "$urlForDownload" "$dt"
   fi

   # run ctjs
   # runCtjs
}

# check if version file and ctjs exists
checkForVersionAndCtjs() {
   local ctjs fileZipName
   ctjs="$cacheFolder/$os_arch/ctjs"
   if [[ -s $versionFile ]] && [[ -s $ctjs ]]; then # ctjs runtime is okay, maybe...
      ctjsVersion=$(grep tag_name <"$versionFile" | tr -d ' ''"'',' | cut -d: -f2)
      # echo "$ctjsVersion"
   elif [[ -s $jsonTempFilePath ]]; then # maybe download is cached
      fileZipName=$(tr -d '"'','' ' <"$jsonTempFilePath" | grep ^name | cut -d: -f2)
      fileZipName+="$cacheFolder/"
      echo "$fileZipName"
      if [[ -s $fileZipName ]]; then
         # extract files
         extractFiles "$fileZipName"
      fi
   elif [[ -s $ctjs ]]; then # or file was extracted
      buildVersionFile
   else
      echo "No ctjs installation found ..."
      return 1
      #exit 1
   fi
}

runCtjs() {
   local ctjs
   ctjs="$cacheFolder/$os_arch/ctjs"
   if [[ -z $ctjsVersion ]]; then
      # if something is wrong, exit the script...
      if ! checkForVersionAndCtjs; then
         return 1
      fi
   fi
   echo ""$'\b'"Running ctjs $ctjsVersion"
   if $ctjs >/dev/null 2>&1; then
      echo "ctjs $ctjsVersion has been terminated by the user"
      exit 0
   else
      echo "An error occurred while running ctjs"
   fi
}

# @param $1 - file name
# @param $2 - url for download
# @param $3 - download command
downloadAndExtractFile() {
   local downloadMessage errorToDownloadFile \
      filePath fileSize
   downloadMessage=$(echo "downloading \
       $RED_COLOR$BOLD_TEXT$1$RESET_ALL_ATTRIBUTES from \
       $GREEN_COLOR$BOLD_TEXT$2$RESET_ALL_ATTRIBUTES" |
      sed -uE 's/\s+/ /g')
   filePath="$cacheFolder/$1"
   fileSize=$(echo "${jsonArray[size]}" | tr -d ',')
   # checks if file is in the cache
   echo ""$'\b'"Checking if file is cached ..."
   if [[ -f "$filePath" &&
      "${fileSize}" -eq "$(du -b "$filePath" | awk '{print $1}')" ]]; then
      # the file is cached, let's use it
      echo "The file is cached, let's use it ..."
      extractFiles "$filePath"
   else
      echo "$downloadMessage"
      if ($3); then
         echo "Download is complete ..."
         extractFiles "$filePath"
         return 0
      else
         errorToDownloadFile="Error to download file"
         # allows globbing
         # shellcheck disable=SC2086
         echo $RED_COLOR$BOLD_TEXT$errorToDownloadFile$RESET_ALL_ATTRIBUTES
         exit 1
      fi
   fi
}

# @param $1 - file path
extractFiles() {
   progress &
   pid=$!
   local errorToExtractFiles filePath workDir tmpFolder
   filePath=$1
   workDir="$cacheFolder/$os_arch"
   tmpFolder="$cacheFolder/tmp"
   if [[ -d "$workDir" ]]; then
      echo ""$'\b'"Removing previous installation ..."
      if rm -rf "$workDir"; then
         echo ""$'\b'"done"
      fi
   fi
   echo ""$'\b'"Checking for temp folder ..."
   if ! [[ -d $tmpFolder ]]; then
      mkdir "$cacheFolder/tmp"
   fi
   echo ""$'\b'"Extracting files ..."
   if cat <"$filePath" | stdbuf -o0 bsdtar -x -C "$tmpFolder"; then
      echo ""$'\b'"Adjusting work folder ..."
      if [[ -d "$tmpFolder/$os_arch" ]]; then
         mv "$tmpFolder/$os_arch" "$cacheFolder"
      else
         mv "$tmpFolder" "$workDir"
      fi
      echo ""$'\b'"done"
      echo ""$'\b'"Removing temporary files"
      if (rm -rf "$filePath"); then
         echo ""$'\b'"done"
         echo ""$'\b'"Adjusting file permissions ..."
         if adjustPermission "$workDir"; then
            echo ""$'\b'"done"
            buildVersionFile
         else
            echo "Error to adjust permission"
            kill "$pid"
         fi
      fi
   else
      errorToExtractFiles=""$'\b'"Error to extract files"
      # allows globbing
      # shellcheck disable=SC2086
      echo $RED_COLOR$BOLD_TEXT$errorToExtractFiles$RESET_ALL_ATTRIBUTES
      kill "$pid"
      exit 1
   fi
}

# @param $1 - work dir
adjustPermission() {
   local files fileBaseName
   files=("$1"/*)
   for file in "${files[@]}"; do
      fileBaseName=$(basename "$file")
      if [[ -d "$file" ]]; then
         echo -n ""$'\b'"Entering in the "
         echo -n "$YELLOW_COLOR$BOLD_TEXT"
         echo -n "$fileBaseName"
         echo -n "$RESET_ALL_ATTRIBUTES "
         echo "directory"
         adjustPermission "$file"
      else
         if file "$file" |
            stdbuf -o0 grep -E \
               "(executable|shared)" >/dev/null; then
            echo -n ""$'\b'"adjusting permission for "
            echo -n "$RED_COLOR$BOLD_TEXT"
            echo -n "$fileBaseName"
            echo "$RESET_ALL_ATTRIBUTES"
            if chmod +x "$file"; then
               echo -n ""$'\b'"$GREEN_COLOR$BOLD_TEXT"
               echo -n "â˜‘"
               echo -n "$RESET_ALL_ATTRIBUTES "
               echo "$fileBaseName"
            else
               echo -n ""$'\b'"$RED_COLOR$BOLD_TEXT"
               echo -n "â˜’"
               echo -n "$RESET_ALL_ATTRIBUTES "
               echo "$fileBaseName"
            fi
         fi
      fi
   done
}

# @param $1 - optional json temp file. Called by jsonHandler
buildVersionFile() {
   local str
   str="{"
   echo ""$'\b'"Building version file ..."
   for key in "${!jsonArray[@]}"; do
      str+="\"$key\": ${jsonArray[$key]}"
      if ! [[ $str =~ [,]$ ]]; then
         str+=","
      fi
      str+=$'\n'
   done
   str+="}"
   if [[ -z $str ]]; then
      echo -n "$RED_COLOR$BOLD_TEXT"
      echo -n "something is wrong, the version file is empty"
      echo "$RESET_ALL_ATTRIBUTES"
      echo "let's check if exists a temp file"
      if [[ -s $jsonTempFilePath ]]; then
         echo "found"
         oldIFS=$IFS
         IFS=$'\n'
         while read -r line; do
            echo "$line"
         done < <($jsonTempFilePath)
         IFS=$oldIFS
      else
         echo -n "$RED_COLOR$BOLD_TEXT"
         echo -n "temp file not found, exiting..."
         echo "$RESET_ALL_ATTRIBUTES"
         exit 1
      fi
   fi
   # called when we need to create a json temp file...
   # it's called in jsonHandler
   if [[ -n $1 ]]; then
      echo "$str" >"$1"
   else
      echo "$str" >"$versionFile"
      echo ""$'\b'"done"
      # check if exists a json temp file
      if [[ -s "$jsonTempFilePath" ]]; then
         # removes json temp file
         rm -rf "$jsonTempFilePath"
      fi
      kill "$pid"
   fi
}

# @param $1 - json from https://api.github.com/repos/ct-js/ct-js/releases/latest
jsonHandler() {
   local found strKey version
   echo "parsing json ..."
   declare -A jsonArray=(
      [tag_name]=""
      [name]=""
      [size]=""
      [browser_download_url]=""
   )
   oldIFS=$IFS
   IFS=$'\n'
   found=false
   while read -r line; do
      line=$(echo "$line" | sed -uE 's/\s+//g')
      if echo "$line" | grep ^'"tag_name"' >/dev/null; then
         jsonArray[tag_name]=$(echo "$line" | cut -d: -f2)
         if [[ -e $versionFile ]]; then
            while read -r l; do
               if echo "$l" | grep '"tag_name"' >/dev/null; then
                  version=$(echo "$l" | cut -d: -f2)
                  if [[ $version =~ ${jsonArray[tag_name]} ]]; then
                     echo -n "$GREEN_COLOR${BOLD_TEXT}"
                     echo -n "It's already latest version"
                     echo "$RESET_ALL_ATTRIBUTES"
                     return 0
                  fi
               fi
            done <"$versionFile"
            echo
         fi
      elif echo "$line" | grep ^'"assets"' >/dev/null; then
         found=true
         continue
      fi
      if "$found"; then
         for key in "${!jsonArray[@]}"; do
            strKey=$(echo "$line" | grep ^"\"${key}\"" | cut -d: -f2-)
            if [[ -n "$strKey" ]]; then
               jsonArray[$key]=$strKey
            fi
         done
         # let's check if $line contains url download
         # from json row
         if echo "$line" | grep "$os_arch" >/dev/null &&
            echo "$line" | grep ^'"browser_download_url"' \
               >/dev/null; then
            break
         fi
      fi
   done < <(echo "$1")
   IFS=$oldIFS
   # let's create a json temp file
   # used by checkForVersionAndCtjs
   buildVersionFile "$jsonTempFilePath"
   echo "done"
   download
}

checkForOsAndArch() {
   local arch archArray found
   found=false
   arch=$(uname -m | tr "[:upper:]" "[:lower:]")
   archArray=("i686" "x86_64" "ia32" "ia64")
   case $kernelName in
   "linux" | "darwin")
      for value in "${archArray[@]}"; do
         if [[ $value == "$arch" ]]; then
            found=true
            break
         fi
      done
      if "$found"; then
         if [[ $os_arch == linux* ]]; then
            # call GNU/Linux OS function
            linux
         else
            # call Mac_OS function
            darwin
         fi
      else
         echo -n "The architecture "
         echo -n "$RED_COLOR$BOLD_TEXT$arch$RESET_ALL_ATTRIBUTES "
         echo "is not supported"
         exit 1
      fi
      ;;
   *)
      echo -n "$RED_COLOR$BOLD_TEXT$kernelName$RESET_ALL_ATTRIBUTES "
      echo "O.S is not supported."
      exit 1
      ;;
   esac
}

checkForDownloadTool() {
   local downloadTool
   downloadTool=$(which wget 2>/dev/null || which curl 2>/dev/null)
   if [[ -z $downloadTool ]]; then
      echo -n "There is no download tool."
      echo -n " Download and install "
      echo -n "$GREEN_COLOR${BOLD_TEXT}wget$RESET_ALL_ATTRIBUTES "
      echo "or $GREEN_COLOR${BOLD_TEXT}curl$RESET_ALL_ATTRIBUTES."
      exit 1
   else
      if [[ $downloadTool =~ (wget)$ ]]; then
         # wget command
         downloadTool="$downloadTool -P $cacheFolder --show-progress -q -o /dev/null -c "
      else
         # curl command
         downloadTool="$downloadTool -# --output-dir $cacheFolder -L "
      fi
   fi
   echo "$downloadTool"
}

checkForUpdate() {
   progress &
   # gets pid of progress
   pid=$!
   local dt returnCode
   # download tool - wget or curl
   if grep "/wget" <(echo "$downloadTool") &>/dev/null; then
      dt="$downloadTool -O - $ctjsReleaseUrl"
   else
      dt="$downloadTool -o - $ctjsReleaseUrl"
   fi
   echo -ne "\bUsing${GREEN_COLOR}${BOLD_TEXT} "
   echo -ne "\b $(cut -d ' ' -f1 <(echo "$dt")) "
   echo "${RESET_ALL_ATTRIBUTES}command ..."
   echo -n "Looking for updates from "
   echo "${GREEN_COLOR}${BOLD_TEXT}$ctjsReleaseUrl${RESET_ALL_ATTRIBUTES}"
   jsonResponse=$($dt 2>&1)
   returnCode=$?
   kill "$pid"
   echo -ne "\b"
   if ((returnCode)); then
      echo -n "${RED_COLOR}${BOLD_TEXT}"
      if [[ $returnCode == 2 ]]; then
         echo -n "$jsonResponse $dt"
      else
         echo -n "No internet connection available?"
      fi
      echo "${RESET_ALL_ATTRIBUTES}"
      # runCtjs
   else
      jsonHandler "$jsonResponse"
   fi
}

